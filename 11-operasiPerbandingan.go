//Sama dengan operasi perbandingan di java
//operasi yang akan menghaislkan nilai T/F

package main

import "fmt"

func main() {

	var no1 = 100
	var no2 = 200

	fmt.Println(no1 > no2)
	fmt.Println(no1 >= no2)
	fmt.Println(no1 < no2)
	fmt.Println(no1 <= no2)
	fmt.Println(no1 == no2)
	fmt.Println(no1 != no2)

}
